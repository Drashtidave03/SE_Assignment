
public class DataTypes {
	public static void main(String[] args) {
		System.out.println("hello");
		
        // Integer types
        byte age = 25;              // 8-bit
        short year = 2025;          // 16-bit
        int population = 1500000;   // 32-bit
        long stars = 10000000000L;  // 64-bit (use 'L' suffix)

        // Floating-point types
        float pi = 3.14f;           // 32-bit (use 'f' suffix)
        double precision = 0.9999;  // 64-bit

        // Other primitives
        char grade = 'A';           // Single 16-bit Unicode character
        boolean isJavaFun = true;   // true or false
		
	}
}
